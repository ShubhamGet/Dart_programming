void main() {
  int num1 = 10;
  var num2 = 10;
  var res = 0;
  res = num1 + num2;
  print("Addition: ${res}");

  res = num1 - num2;
  print("Subtraction: ${res}");

  res = num1 * num2;
  print("Multiplication: ${res}");

  res = num1 - num2;
  print("Division: ${res}");

  res = num1 % num2;
  print("MODULS: ${res}");

  num1++;
  print("Increment : $num1");

  num1--;
  print("Decrement : $num1");
}
